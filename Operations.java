package basic_additions;

public class Operations 
{
	public static void main(String[] args)
	{
		int no1 = 20;
		int no2 = 10;
		int sum = no1+no2;
		int sub = no1-no2;
		int mul = no1 * no2;
		int div = no1/no2;
		int mod = no1 % no2;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
		
		
	}

}
