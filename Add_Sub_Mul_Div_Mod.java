package basic_additions;

public class Add_Sub_Mul_Div_Mod {
	
	static void add()
	{
		int no1 = 20;
		int no2 = 10;
		
		int sum = no1 + no2;
		System.out.println(sum);
	}
	static void sub()
	{
		int no1 = 20;
		int no2 = 10;
		
		int sub = no1 - no2;
		System.out.println(sub);
	}
	static void mul()
	{
		int no1 = 20;
		int no2 = 10;
		
		int mul = no1 * no2;
		System.out.println(mul);
	}
	static void div()
	{
		int no1 = 20;
		int no2 = 10;
		
		int div = no1 / no2;
		System.out.println(div);
	}
	static void mod()
	{
		int no1 = 20;
		int no2 = 10;
		
		int mod = no1 % no2;
		System.out.println(mod);
	}

	public static void main(String[] args) {

		add();
		sub();
		mul();
		div();
		mod();

	}

}
