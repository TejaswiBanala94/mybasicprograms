package assignment1;

public class Default_Value_of_Global_Variables
{
	static int a;
	double b;
	static String name;
	char c;
	static boolean d;

	public static void main(String[] args) 
	{
		System.out.println(name);
		System.out.println(a);
		System.out.println(d);
		Default_Value_of_Global_Variables d1 = new Default_Value_of_Global_Variables();
		System.out.println(d1.c);
		System.out.println(d1.b);
		
		

	}

}
