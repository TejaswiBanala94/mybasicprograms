package assignment1;

public class String_EqualsIgnore_case 
{
	public static void main(String[] args) 
	{
		String s1 = "Tejaswi";
		System.out.println(s1.equalsIgnoreCase("tejaswi")); //equalsIgnoreCase
		
		
		String s2 = "";
		System.out.println(s2.isEmpty());//isEmpty
		
		String s3 = "teju";
		System.out.println(s3.endsWith("i")); //endswith
		
		String q = "software";
		String answer = q.repeat(10);//repeat
		System.out.println(answer);
		
	}

}
